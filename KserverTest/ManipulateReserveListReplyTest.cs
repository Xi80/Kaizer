using KaizerCommanderApi;

namespace KserverTest;

[TestClass]
public class ManipulateReserveListReplyTest
{
    [TestMethod]
    public void TestMethod1()
    {
        var ms = new MemoryStream(new byte[]
        { 
            0x91, 0x11,
            0x0, 0x7c, 0x0, 0x0, 0x0, 0x3, 0x64, 0xff, 0xcc, 0x74, 0xb, 0x59, 0x58, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
            0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
            0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x64, 0xff, 0xcc, 0x7e, 0xb, 0x59, 0x58,
            0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x64, 0xff, 0xcc, 0x80,
            0xb, 0x59, 0x58, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x64,
            0xff, 0xcc, 0x82, 0xb, 0x59, 0x58, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
            0x0, 0x0, 0x0
        });
        var r = new ManipulateReserveListReply();
        r.SetValueFromMemoryStream(ms);
        Assert.AreEqual(r.GetReplyId(),0x9111);
        Assert.AreEqual(r.GetResult(),0);
        Assert.AreEqual(r.GetNumberOfReserveSongs(),3);
        Assert.AreEqual(r.GetPlayingSongInformation().GetTrayNo(),2905);
        Assert.AreEqual(r.GetPlayingSongInformation().GetChapterNo(),88);
        Assert.AreEqual(r.GetReserveSongInformationList()[2].GetTrayNo(),2905);
        Assert.AreEqual(r.GetReserveSongInformationList()[2].GetChapterNo(),88);
    }
}