using KaizerCommanderApi;

namespace KserverTest;

[TestClass]
public class SongInformationTest
{
    [TestMethod]
    public void TestMethod1()
    {
        var ms = new MemoryStream(new byte[]
        {
            0x64, 0xff, 0xcc, 0x74, 0xb, 0x59, 0x58, 0x0,
            0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
            0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
            0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
            0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
            0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0
        });

        var s = new SongInformation();
        s.SetValueFromMemoryStream(ms);
        Assert.AreEqual(s.GetTrayNo(),2905);
        Assert.AreEqual(s.GetChapterNo(),88);
    }
}